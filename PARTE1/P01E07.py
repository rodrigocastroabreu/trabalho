# 7) Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
# Calcule e mostre o total do seu salário no referido mês.

var_quanto_ganha_hora = float(input("Quanto ganhar por hora?\n "));

var_numero_horas_trabalhadas = float(input("Número de horas trabalhadas?"));

var_salario_total = var_quanto_ganha_hora * var_numero_horas_trabalhadas;

print("Salario total:\n ",var_salario_total);
