#6) Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

var_lateral = float(input("Digite valor de um dos lados do quadrado em metros: \n"));

var_area = var_lateral * var_lateral;

var_dobro_area = var_area * 2;

print("A área é \t", var_area);

print("O dobro da área é: \t", var_dobro_area);

