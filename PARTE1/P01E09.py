# 9) Faça um Programa que peça a temperatura em graus Celsius,
# transforme e mostre em graus Fahrenheit.

var_temperatura_Celsius = float(input("Qual temperatura em graus Celsius?\n"));

var_temperatura_Fahrenheit = (var_temperatura_Celsius * 9/5) + 32;

print("A temperatura em graus Fahrenheit é:\n",var_temperatura_Fahrenheit);
